package model;

public class BusStopRoadTileModel extends RoadTileModel {

	@SuppressWarnings("unused")
	private Integer mainDirection;
	private String tileType = "BusStopRoadTile";

	/*******************************************************************/
	/**  Class BusStopRoadTileModel                                   **/
	/**                                                               **/ 
	/**  road tile models a bus stop                                  **/ 
	/*******************************************************************/
	
	public BusStopRoadTileModel(int posX, int posY, Integer direction)
	{
		super(posX, posY, direction);
		this.mainDirection = direction;
		setTileType(tileType);
		
	}

	/*******************************************************************/
	/**  Getter, Setter                                       		  **/
	/**                                                               **/ 
	/*******************************************************************/

	public String getTileType() {
		return tileType;
	}
	public void setTileType(String tileType) {
		this.tileType = tileType;
	}

}
